package com.example.litepaltest;

public class Category {
    private int id;
    private String categoryName;
    private int categoruCode;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    public int getCategoruCode() {
        return categoruCode;
    }

    public void setCategoruCode(int categoruCode) {
        this.categoruCode = categoruCode;
    }
}
